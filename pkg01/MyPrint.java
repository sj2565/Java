package pkg01;
/*
 여러줄 주석
 */
public class MyPrint {

	public static void main(String[] args) {
		// 한 줄 주석
		System.out.print("홍");
		System.out.println("");
		System.out.print('길');
		System.out.println("동");
		
		System.out.println("대한민국 파이팅");
		
		System.out.println("\"예체능\" 메인 MC '강호동'"); // \", \' : 큰,작은 따옴표 출력
		
		System.out.println("학생들 시험 점수");
		System.out.println("이름\t국어\t영어\t수학"); // \n : 줄바꿈
		System.out.println("김철수\t50\t50\t60");
		System.out.println("박영희\t40\t50\t60");
	}
}